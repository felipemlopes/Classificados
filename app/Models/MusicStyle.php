<?php

namespace App\Models;

use Illuminate\Database\Eloquent\Model;

class MusicStyle extends Model
{
    /**
     * The attributes that are mass assignable.
     *
     * @var array
     */
    protected $fillable = [
        'id', 'name', 'slug'
    ];

    /**
     * The table associated with the model.
     *
     * @var string
     */
    protected $table = 'music_styles';

    protected $dates = [
        'created_at',
        'updated_at',
    ];



    /*
   |--------------------------------------------------------------------------
   | FUNCTIONS
   |--------------------------------------------------------------------------
   */


    /*
   |--------------------------------------------------------------------------
   | RELATIONS
   |--------------------------------------------------------------------------
   */
    public function artists()
    {
        return $this->belongsToMany('App\Models\Artist','artist_musical_styles');
    }


